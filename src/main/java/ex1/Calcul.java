package ex1;

public class Calcul {

	/** Effectue une opération d'addition
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	
	/** Effectue une opération de soustraction
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
